    public void loadtable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tb_hoaDon.getModel();
        ArrayList<HoaDon> ds = this.donService.all();
        dtm.setRowCount(0);
        for (HoaDon hv : ds) {
            Object[] rowdata = new Object[]{
                hv.getId_hoa_don(),
                hv.getId_khach_hang(),
                hv.getId_nhan_vien(),
                hv.getId_voucher(),
                hv.getNgay_thanh_toan(),
                hv.getSdt(),
                hv.getDia_chi(),
                hv.getPhi_ship(),
                hv.getTong_tien(),
                hv.getCreated_at(),
                hv.getUpdated_at(),
                hv.getCreated_by(),
                hv.getUpdated_by(),
                hv.getHinh_thuc_thanh_toan(),
                hv.getTrang_thai() == 1 ? "da thanh toan" : "chua thanh toan",};
            dtm.addRow(rowdata);
        }
    }

