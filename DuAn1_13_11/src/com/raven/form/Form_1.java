    private void tb_hoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_hoaDonMouseClicked
        // TODO add your handling code here:
        txt_hoadon.setText("");
        txt_khachhang.setText("");
        txt_nhanvien.setText("");
        txt_vourcher.setText("");

        txt_sdt.setText("");
        txt_diachi.setText("");
        txt_tongtien.setText("");
        txt_phiship.setText("");
        //        jd_created_at.setDate((Date) tb_hoaDon.getValueAt(row, 9));
        //        jd_updated_at.setDate((Date) tb_hoaDon.getValueAt(row, 10));
        txt_created_by.setText("");
        txt_updated_by.setText("");
        cbb_thanhtoan.setSelectedItem("");
        buttonGroup1.clearSelection();
        int row = this.tb_hoaDon.getSelectedRow();
        if (row == -1) {
            return;
        }
        txt_hoadon.setText(tb_hoaDon.getValueAt(row, 0).toString());
        txt_khachhang.setText(tb_hoaDon.getValueAt(row, 1).toString());
        txt_nhanvien.setText(tb_hoaDon.getValueAt(row, 2).toString());
        txt_vourcher.setText(tb_hoaDon.getValueAt(row, 3).toString());
        jd_ngaythanhtoan.setDate((Date) tb_hoaDon.getValueAt(row, 4));
        txt_sdt.setText(tb_hoaDon.getValueAt(row, 5).toString());
        txt_diachi.setText(tb_hoaDon.getValueAt(row, 6).toString());
        txt_tongtien.setText(tb_hoaDon.getValueAt(row, 7).toString());
        txt_phiship.setText(tb_hoaDon.getValueAt(row, 8).toString());
        jd_created_at.setDate((Date) tb_hoaDon.getValueAt(row, 9));
        jd_updated_at.setDate((Date) tb_hoaDon.getValueAt(row, 10));
        txt_created_by.setText(tb_hoaDon.getValueAt(row, 11).toString());
        txt_updated_by.setText(tb_hoaDon.getValueAt(row, 12).toString());
        if (tb_hoaDon.getValueAt(row, 13).equals("chuyen khoan")) {
            cbb_thanhtoan.setSelectedItem("chuyen khoan");
        }
        if (tb_hoaDon.getValueAt(row, 13).equals("tien mat")) {
            cbb_thanhtoan.setSelectedItem("tien mat");
        } else {
            cbb_thanhtoan.setSelectedItem("");
        }
        if (tb_hoaDon.getValueAt(row, 14).equals("da thanh toan")) {
            rd_dathanhtoan.setSelected(true);
        } else if (tb_hoaDon.getValueAt(row, 14).equals("chua thanh toan")) {
            rd_chuathanhtoan.setSelected(true);
        } else {
            buttonGroup1.clearSelection();
        }

    }//GEN-LAST:event_tb_hoaDonMouseClicked

